package Oops.Inheritance.MultiLevelInheritance;

public class box {
    double length;
    double width;
    double height;

    box() {
        this.length = -1;
        this.width = -1;
        this.height = -1;
    }

    box(double length, double width, double height) {
        this.length = length;
        this.width = width;
        this.height = height;
    }
}
